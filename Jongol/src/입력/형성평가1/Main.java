package 입력.형성평가1;

/**
 * 세 개의 정수형 변수를 선언하고 각 변수에 10 20 30을 대입한 후 그 변수를 이용하여 출력 예와 같이 출력하는 프로그램을 작성하시오.
 * 10 + 20 = 30
 */
public class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        System.out.println(a + " + " + b + " = " + c);
    }
}
