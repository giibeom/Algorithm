package 출력.형성평가4;

/**
 * kor 90
 * mat 80
 * eng 100
 * sum 270
 */
public class Main {
    public static void main(String[] args) {
        int kor = 90;
        int mat = 80;
        int eng = 100;
        System.out.println("kor " + kor);
        System.out.println("mat " + mat);
        System.out.println("eng " + eng);
        System.out.println("sum " + (kor + mat + eng));
    }
}
